
public class MonthlyReport {

    String product;
    boolean isExpenses;
    int quantity;
    double sumOne;

    public MonthlyReport(String product, boolean isExpenses, int quantity, double sumOne) {
        this.product = product;
        this.isExpenses = isExpenses;
        this.quantity = quantity;
        this.sumOne = sumOne;
    }
}












